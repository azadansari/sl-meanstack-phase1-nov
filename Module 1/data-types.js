// Number data type
var xyz = 10;
console.log(typeof xyz);
console.log(xyz);


// String data type
var str = "this is a string";
console.log(typeof str);
console.log(str);

// Array data type
var arr = ["BMW", "AUDI", "SCODA"];
console.log(typeof arr);
console.log(arr);

// Object data type
var obj = {name:'Azad', city: 'Ramgarh'};
console.log(typeof obj);
console.log(obj);

// Undefine
var und;
console.log(typeof und);
console.log(und);

// Null
var n = null;
console.log(typeof n);
console.log(n);